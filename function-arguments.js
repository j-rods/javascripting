// define a function math that takes 3 arguments
// within math function, return the value obtained
// from multiplying the second and third arguments
// and adding the result to the first argument
function math(num1, num2, num3) {
  return num1 + (num2 * num3);
}

//call math() function with the number 53 as first argument,
// number 61 as second argument, 67 as third argument
console.log(math(53, 61, 67));